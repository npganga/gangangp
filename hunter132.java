package Hunter;

import java.util.Scanner;

public class hunter132 {
public static void main(String[] args){
	Scanner in=new Scanner(System.in);
	int n=in.nextInt();
	for(int i=n;i>0;i--){
		for(int k=0;k<2;k++){
		for(int j=i;j>0;j--){
			System.out.print("*");
		}
		System.out.print(" ");
		}
		System.out.print("\n");
	
	
	}
}
}
